class Usuario {
  constructor(
    id,
    fechaNacimiento,
    Pais,
    Nombre,
    Apellido,
    NombrePantalla,
    Correo,
    Contraseña
  ) {
    this.id = id;
    this.fechaNacimiento = fechaNacimiento;
    this.Pais = Pais;
    this.Nombre = Nombre;
    this.Apellido = Apellido;
    this.NombrePantalla = NombrePantalla;
    this.Correo = Correo;
    this.Contraseña = Contraseña;
  }
}
